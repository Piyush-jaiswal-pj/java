package sct;

public class typecasting {

	public static void main(String[] args) {
	int a=100;
	double c=a;//automatic typecast
	float b=a;
	double e=5.5;
	int f=(int)e;//manual typecaste
	System.out.println("the type cast  of int into double is "+c);
	System.out.println("the type cast  of int into float is "+b);
	System.out.println("the type cast  double into int is "+f);

	}

}
