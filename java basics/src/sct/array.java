package sct;
// traversing of array
//single dimensional
public class array {

	public static void main(String[] args) {
		int a[]= new int [5];//declaration and instantation
		a[0]=10;
		a[1]=20;
		a[2]=30;
		a[3]=40;
		a[4]=50;
		
		System.out.println("The traverse of array is :");
		for(int i=0;i<5;i++)
		{
			System.out.println(a[i]);//traversing logic
		}
		

	}

}
