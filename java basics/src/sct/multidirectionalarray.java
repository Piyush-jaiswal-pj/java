package sct;

public class multidirectionalarray {

	public static void main(String[] args) {
		int arr[][]= {{1,2,3},{4,5,6},{7,8,9}};//dec and initia of 2d array
		for(int i=0;i<arr.length;i++)
		{
			for(int j=0;j<arr.length;j++)
				System.out.print(arr[i][j]+" ");//print 2d array remember the use of print not println
		
		System.out.println();
		}

	}

}
